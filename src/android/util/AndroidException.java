package android.util;
public class AndroidException extends java.lang.Exception {
public  AndroidException() { super (); }
public  AndroidException(java.lang.String name) { super(name); }
public  AndroidException(java.lang.String name, java.lang.Throwable cause) { super(name, cause); }
public  AndroidException(java.lang.Exception cause) { super(cause); }
}
